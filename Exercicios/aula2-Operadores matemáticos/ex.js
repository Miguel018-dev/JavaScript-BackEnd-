/* Operadores Matemáticos.
+: soma
-: subtração
*: multiplicação
/: divisão
%: resto da divisão
**: exponenciação
*/

let A = 9
let B = 8

soma = A + B
console.log(`O resultado da soma é ${soma}`)

A = 7
B = 5

subtra = A - B
console.log(`O resultado da subtração é ${subtra}.`)


A = 6
B = 4

mult = A * B
console.log(`O resultado da multiplicação é ${mult}.`)

A = 10
B = 5

divi = A / B
console.log(`O resultado da divisão é ${divi}.`)

A = 22
B = 8

res = A % B
console.log(`O resto da divisão é ${res}.`)

A = 4
B = 2

res = A ** B
console.log(`O resultado da exponenciação é ${res}`)