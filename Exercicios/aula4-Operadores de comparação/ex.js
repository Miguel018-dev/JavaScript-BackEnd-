/* Operadores de comparação.
> maior que               
< menor que
>= maior ou igual                            
<= menor ou igual
== igual
=== idêntico (o valor e o tipo de variável)
!= diferente
*/

let A = 7
let B = 10

res = A > B
console.log(`${res}`)

res = A < B
console.log(`${res}`)

A = 8
B = 8

res = A == B 
console.log(`${res}`)

res = A === B
console.log(`${res}`)

A = 9
B = 27

res = A != B
console.log(`${res}`)

